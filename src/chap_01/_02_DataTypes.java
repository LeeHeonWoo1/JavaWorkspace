package chap_01;

public class _02_DataTypes {
    // public static void main 의 앞글자만 따서 psvm이라고 작성하면 바로 아래의 코드가 완성
    // main + enter 도 가능
    public static void main(String[] args) {
        // 무조건 따옴표는 쌍따옴표
        System.out.println("Hello World!"); //str
        System.out.println("안녕하세요!"); //str
        System.out.println(12);//int
        System.out.println(-35);//int
        System.out.println(0.33);//float
        System.out.println(1/3);//devide
        System.out.println(1+3);//plus
        System.out.println(2*5);//multiple
        System.out.println(true);//bool.true
        System.out.println(false);//bool.false
        System.out.println();
    }
}
