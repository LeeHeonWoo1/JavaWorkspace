package chap_08;
import java.lang.String;

public class _01_package {
    // package : 클래스와 인터페이스의 집합을 의미한다. 서로 관련이 있는 클래스나 인터페이스를 함께 묶음으로서 파일을 효율적으로 관리할 수 있다.
    // 협업 시 서로 작업한 클래스 사이에서 발생할 수 있는 이름 충돌 문제까지도 패키지를 이용하면 피할 수 있다.
    // 다음은 자바에서 가장 많이 사용되는 패키지 중 하나인 String 클래스의 패키지 이름이다. : java.lang.String
    // 이러한 패키지를 불러오는 방식은 아래 두 가지가 있다.
    // import 패키지이름.클래스이름; => 특정 클래스만 이용하고자 하는 경우
    // import 패키지이름.*; => 해당 패키지의 모든 크래스를 클래스 이름만으로 사용하고자 하는 경우
}
